/* TABLA QUE ALMACENA LA PARTE 1 DEL INVENTARIO MAESTRO QUE HACE REFERENCIA A LOS MATERIALES E INSUMOS */
CREATE TABLE IF NOT EXISTS maestroinventariounomi (
