/* TABLA QUE ALMACENA LA PARTE 2 DEL INVENTARIO MAESTRO QUE HACE REFERENCIA A LOS PRODUCTOS EN PROCESO */
CREATE TABLE IF NOT EXISTS maestroinventariodospp (
